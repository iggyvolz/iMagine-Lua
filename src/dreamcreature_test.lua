describe("DreamCreature",function()
  describe("named furok",function()
    describe("with owner tony",function()
      thedreamcreature=dreamcreature("furok","tony")
      it("should have the owner \"tony\".",function()
        assert.are.same("tony",thedreamcreature.owner)
      end)
